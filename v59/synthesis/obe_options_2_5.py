# ===========================================================================
# OPTION 5 : dimension of the associative algebra generated by a rep
# ===========================================================================
def algebra_dim(gens, tol=1e-8):
    """Dimension of the unital associative algebra generated by the matrices in
    `gens` (under composition), via incremental Gram-Schmidt on flattened ops."""
    n = gens[0].shape[0]
    dmax = n * n
    Q = np.zeros((dmax, dmax))      # orthonormal flattened basis (rows)
    state = {"k": 0}
    basis = []
    work = deque()

    def add(M):
        v = M.flatten().astype(float).copy()
        k = state["k"]
        if k:
            v -= Q[:k].T @ (Q[:k] @ v)
        nv = np.linalg.norm(v)
        if nv > tol:
            Q[k] = v / nv
            state["k"] = k + 1
            basis.append(M)
            work.append(M)
            return True
        return False

    add(np.eye(n))
    for g in gens:
        add(g)
    while work:
        B = work.popleft()
        for g in gens:
            add(B @ g)
            add(g @ B)
        if state["k"] >= dmax:
            break
    return state["k"]


