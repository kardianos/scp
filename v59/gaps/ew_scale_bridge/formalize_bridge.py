def algebra_dim(gens, tol=1e-8):
    """Dim of the unital associative algebra generated by `gens` (composition),
    via incremental Gram-Schmidt on flattened operators."""
    n = gens[0].shape[0]
    dmax = n * n
    Q = np.zeros((dmax, dmax))
    state = {"k": 0}
    work = deque()

    def add(M):
        v = M.flatten().astype(float).copy()
        k = state["k"]
        if k:
            v -= Q[:k].T @ (Q[:k] @ v)
        nv = np.linalg.norm(v)
        if nv > tol:
            Q[k] = v / nv
            state["k"] = k + 1
            work.append(M)
            return True
        return False

    add(np.eye(n))
    for g in gens:
        add(g)
    while work:
        B = work.popleft()
        for g in gens:
            add(B @ g)
            add(g @ B)
        if state["k"] >= dmax:
            break
    return state["k"]


def so_n_gens(n):
    """so(n) generators in defining rep: E_ab - E_ba."""
    out = []
    for a in range(n):
        for b in range(a + 1, n):
            M = np.zeros((n, n))
            M[a, b] = 1.0
            M[b, a] = -1.0
            out.append(M)
    return out


def adjoint_ops(gens_def):
    """Adjoint rep matrices (ad_X)_{ij} = <B_i,[X,B_j]> on the orthonormal
    Frobenius basis B = gens/sqrt2 (works for any so(n) generator list)."""
    Bs = [M / np.sqrt(2.0) for M in gens_def]
    nB = len(Bs)
    out = []
    for X in gens_def:
        A = np.zeros((nB, nB))
        for j, Bj in enumerate(Bs):
            C = X @ Bj - Bj @ X
            for i, Bi in enumerate(Bs):
                A[i, j] = np.sum(Bi * C)
        out.append(A)
    return out


