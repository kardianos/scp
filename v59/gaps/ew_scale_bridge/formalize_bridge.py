def invariants(Y):
    YYt = Y @ Y.T
    I1 = np.sum(Y * Y)            # Frobenius^2 = Tr(Y Y^T)
    I2 = np.trace(Y) ** 2
    I3 = np.trace(YYt @ YYt)
