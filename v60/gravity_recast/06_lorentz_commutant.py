def octonion_mult(x, y):
    """Cayley–Dickson octonion product of two length-8 real vectors.
    O = H ⊕ H with (a,b)(c,d) = (ac - conj(d) b,  d a + b conj(c))."""
    def qconj(q):
        return np.array([q[0], -q[1], -q[2], -q[3]])

    def qmul(p, q):
        a1, b1, c1, d1 = p
        a2, b2, c2, d2 = q
        return np.array([
            a1*a2 - b1*b2 - c1*c2 - d1*d2,
            a1*b2 + b1*a2 + c1*d2 - d1*c2,
            a1*c2 - b1*d2 + c1*a2 + d1*b2,
            a1*d2 + b1*c2 - c1*b2 + d1*a2,
        ])
    a, b = x[:4], x[4:]
    c, d = y[:4], y[4:]
    left = qmul(a, c) - qmul(qconj(d), b)
    right = qmul(d, a) + qmul(b, qconj(c))
    return np.concatenate([left, right])


def left_mult_matrices():
    """8x8 left-multiplication matrices L_k (k=0..7); L_0 = I (real unit)."""
    e = [np.eye(8)[i] for i in range(8)]
    L = []
    for k in range(8):
        M = np.zeros((8, 8))
        for j in range(8):
            M[:, j] = octonion_mult(e[k], e[j])
        L.append(M)
    return L


