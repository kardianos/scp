def commutant_dim(generators, n=8):
    """Dimension of {M in R^{n x n} : [M, G] = 0 for all G in generators}."""
    rows = []
    I = np.eye(n)
    for G in generators:
        # [M,G] = M G - G M ; vectorize M (n^2). Linear map: (G^T ⊗ I - I ⊗ G)
        K = np.kron(G.T, I) - np.kron(I, G)
        rows.append(K)
    A = np.vstack(rows)
    # nullspace dimension
    u, s, vh = np.linalg.svd(A)
    tol = 1e-9 * max(A.shape)
    rank = int(np.sum(s > tol))
    return n * n - rank


